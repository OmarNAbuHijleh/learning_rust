/*
    "fn" is how we denote a function definition
    There must be a main() function in every Rust program. The "main" function is what is first run
    You define code blocks using {}
    Note that Rust uses ";"
    The ! at the end of println denotes that this is a macro. It's similar to a function but slightly different. It's convention to use ! at the end of macros when naming them

*/

fn main() {
    println!("Hello, world!");
}
